import turtle
t=turtle.Turtle()
s=turtle.Screen()
s.bgcolor("red")
t.color("White")
t.speed(1)
t.shape('turtle')
t.begin_fill()
t.fillcolor("Yellow")
t.circle(50,steps=4)
t.end_fill()
t.goto(0,-100)
#t.pendown()
#t.reset()
t.begin_fill()
t.fillcolor("blue")
t.circle(50)
t.end_fill()
t.begin_fill()
t.fillcolor("pink")
t.goto(100,-100)
t.circle(50,steps=5)
t.end_fill()
turtle.done()
